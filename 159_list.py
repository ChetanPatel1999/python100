# Sort List Alphanumerically  
# List objects have a sort() method that will sort the list alphanumerically, 
# ascending, by default: Eg.Sort the list alphabetically: 
list1 = ["orange", "mango", "kiwi", "pineapple", "banana"] 
list1.sort() 
print(list1)


#sort assending  order element
list1 = [100, 50, 65, 82, 23]  
list1.sort()  
print(list1) 
# its reverse list
list1.reverse()
print(list1) 

l2=[12,7,3,2,89]
l2.reverse()
print(l2)

# its sort list but decending order
l2.sort(reverse=True)
print(l2)