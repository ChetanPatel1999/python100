# The list() Constructor
#  It is also possible to use the list() constructor when creating a new list. 

marks=[12,45,67,89,34]

fruits=list(("orange","mango","apple","banana"))
l1=list("home")
l2=list({23,45,67,89})
l3=list({"pen":10,"book":50})
l4=list((78,89,100))
print(l4)
print(l1)
print(l2)
print(l3)
print(marks)
print(fruits)
print(type(fruits))